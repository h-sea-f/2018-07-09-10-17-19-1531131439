package practice07;

public class Klass {
    protected int klass;
    public int getKlass() {
        return klass;
    }
    public Klass(int klass){
        this.klass=klass;
    }
    public int getNumber(){
        return klass;
    }
    public String getDisplayName(){
        return "Class "+klass;
    }
}
